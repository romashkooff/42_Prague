int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb < 0)
		return (0);
	while (i * i != nb && i * i <= 2147483647)
	{
		if (i > nb)
			return (0);
		i++;
	}
	return (i);
}

